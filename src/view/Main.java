    private void btSxTheoTen3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSxTheoTen3ActionPerformed
        listBDK.sort(new Comparator<BangDangKy>() {
            @Override
            public int compare(BangDangKy o1, BangDangKy o2) {
                String[] name1 = o1.getTenSV().trim().toLowerCase().split("\\s+");
                String[] name2 = o2.getTenSV().trim().toLowerCase().split("\\s+");
                if (name1[name1.length-1].equals(name2[name2.length-1])) {
                    return o1.getTenSV().compareToIgnoreCase(o2.getTenSV());
                }
                else {
                    return name1[name1.length-1].compareTo(name2[name2.length-1]);
                }
            }
        });
        showBDK(listBDK);
    }//GEN-LAST:event_btSxTheoTen3ActionPerformed

