    private void btCapnhat3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCapnhat3ActionPerformed
        List<SinhVien> sv = IOFile.doc(fSV);
        List<MonHoc> mh = IOFile.doc(fMH);
        for (SinhVien sinhVien : sv) {
            jComboBox2.addItem(sinhVien.getMa() + "");
        }
        for (MonHoc monHoc : mh) {
            jComboBox3.addItem(monHoc.getMa() + "");
        }
        try {
            int svma = Integer.parseInt(jComboBox2.getSelectedItem().toString());
            int mhma = Integer.parseInt(jComboBox3.getSelectedItem().toString());
            String tensv = null, tenmh = null;
            for (SinhVien sinhVien : sv) {
                if (sinhVien.getMa() == svma) {
                    jTextField9.setText(sinhVien.getTen());
                    tensv = sinhVien.getTen();
                    break;
                }
            }
            for (MonHoc monHoc : mh) {
                if (monHoc.getMa() == mhma) {
                    jTextField11.setText(monHoc.getTen());
                    tenmh = monHoc.getTen();
                    break;
                }
            }
            int count = 0;
            for (BangDangKy bangDangKy : listBDK) {
                if (bangDangKy.getMaSV() == svma) {
                    count++;
                    if (count > 8) {
                        throw new GioihanException();
                    }
                    if (bangDangKy.getMaMH() == mhma) {
                        throw new TrungException();
                    }
                }
            }
            Date d = new Date();
            SimpleDateFormat fomatter = new SimpleDateFormat("HH:mm:ss dd/MM/yyyy");
            String thoiGian = fomatter.format(d);
            jTextField8.setText(thoiGian);
            BangDangKy b;
            b = new BangDangKy(svma, mhma, tensv, tenmh, thoiGian);
            listBDK.add(b);
            tmBDK.addRow(b.toObjects());
            ButtonState3(true);
            jComboBox3.removeAllItems();
            jComboBox2.removeAllItems();
            jTextField9.setText("");
            jTextField11.setText("");
            jTextField8.setText("");
        } catch (GioihanException e) {
            JOptionPane.showMessageDialog(this, "Sinh vien khong the dang ky qua 8 mon");
            
        } catch (TrungException e) {
            JOptionPane.showMessageDialog(this, "Sinh vien da dang ky mon hoc nay!");
        }


    }//GEN-LAST:event_btCapnhat3ActionPerformed

