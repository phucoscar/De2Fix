    private void btCapnhat1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCapnhat1ActionPerformed
        if (them) {
            try {
                String ten = jTextField2.getText();
                String st = jTextField3.getText();
                if (ten.isEmpty() || st.isEmpty()) {
                    throw new TrongException();
                }
                int soTiet = Integer.parseInt(st);
                String loai = jComboBox1.getSelectedItem().toString();
                MonHoc m = new MonHoc(ten, soTiet, loai);
                tmMH.addRow(m.toObjects());
                listMH.add(m);
                showMH(listMH);
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jComboBox1.setSelectedIndex(0);
                them = false;
                ButtonState1(true);
                return;
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Vui long nhap so");
                jTextField3.requestFocus();
                return;
            } catch (TrongException e) {
                JOptionPane.showMessageDialog(this, "Khong duoc de trong!");
                if (jTextField2.getText().isEmpty()) {
                    jTextField2.requestFocus();
                } else {
                    jTextField3.requestFocus();
                }
                return;
            }
        }

        if (sua) {
            int row = jTable1.getSelectedRow();
            if (row >= 0 && row <= jTable1.getRowCount() - 1) {
                jTextField2.requestFocus();
                try {
                    String ten = jTextField2.getText();
                    String st = jTextField3.getText();
                    if (ten.isEmpty() || st.isEmpty()) {
                        throw new TrongException();
                    }
                    int soTiet = Integer.parseInt(st);
                    String loai = jComboBox1.getSelectedItem().toString();
                    tmMH.setValueAt(ten, row, 1);
                    tmMH.setValueAt(st, row, 2);
                    tmMH.setValueAt(loai, row, 3);
                    int ma = Integer.parseInt(jTextField1.getText());
                    MonHoc m = new MonHoc(ma, ten, soTiet, loai);
                    listMH.set(row, m);
                    //showMH(listMH);
                    ButtonState1(true);
                    sua = false;
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jComboBox1.setSelectedIndex(0);
                } catch (TrongException e) {
                    JOptionPane.showMessageDialog(this, "Khong duoc de trong!");
                    if (jTextField2.getText().isEmpty()) {
                        jTextField2.requestFocus();
                    } else {
                        jTextField3.requestFocus();
                    }
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "Vui long nhap so");
                    jTextField3.requestFocus();
                    return;
                }
            } else {
                JOptionPane.showMessageDialog(this, "Vui long chon mon hoc de xoa!");
                return;
            }
        }
    }//GEN-LAST:event_btCapnhat1ActionPerformed

